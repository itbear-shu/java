package day_6;

public class Person01 {
    private String name;
    private int age;
    public Person01() {

    }

    public Person01(int age) {
        this.age = age;
    }

    public Person01(String name) {
        this.name = name;
    }

    public Person01(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public Person01(int age, String name) {
        this.name = name;
        this.age = age;
    }
}
